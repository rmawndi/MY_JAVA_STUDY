package icehs.science.chapter08;

public class idol {
	private String group;
	private String name;
	
	public idol(String group, String name) {
		this.group = group;
		this.name = name;
	}
	public String getGroup() {
		return group;
	}
	public String getName() {
		return name;
	}
	
	
	
	
	

}
