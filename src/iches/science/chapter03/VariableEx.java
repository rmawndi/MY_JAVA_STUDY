package iches.science.chapter03;

// 어머니의 생신인 1960년 6월 20일을 저장하는 변수를 만들어 값을 지정하고, 저장된 값을 확인한다.
public class VariableEx {

	public static void main(String[] args) {
		int bornYear;
		bornYear = 1960;
		
		int bornMonth;
		bornMonth = 6;
		
		int bornDay = 20;
		
		System.out.println(bornYear);
		System.out.println(bornMonth);
		System.out.println(bornDay);
		
	}

}
